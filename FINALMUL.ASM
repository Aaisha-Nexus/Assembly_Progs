DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
        ;TAKING FIRST INPUT
        MOV AH,1
        INT 21H
        SUB AL,48 ; ADJUSTING ASCII
        MOV BL,AL ;STORE MULTIPLICAND IN AL AND MOVE IN BL

        ;TAKING ANOTHER INPUT
        MOV AH,1
        INT 21H
        SUB AL,48
        MOV CL, AL ; MOV SECOND INPUT IN CL REGISTER

        MOV AL, BL; MOVE BL TO AL AS MULTIPLICAND HAS TO BE IN AL
        MUL CL ; HERE AL=AL*CL
        AAM ; TO STORE RESULTANT IF 2 DIGITS

        MOV CH, AH   ; AH FOR TENS GOES TO CH
        MOV CL,AL     ; AL FOR ONES GOES TO CL
        MOV DL,CH ; MOVE CH TO DL
        ADD DL,48 ; AGAIN ADJUST THE ASCII
        MOV AH,2
        INT 21H

        MOV DL,CL ; MOVE CL TO DL
        ADD DL,48
        MOV AH,2
        INT 21H

        MOV AH,4CH
        INT 21H

        MAIN ENDP
        END MAIN
