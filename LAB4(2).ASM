DOSSEG
;TO INPUT A STRING AND PRINT IT
.MODEL SMALL
.STACK 100H
.DATA

STR1 DB 100 DUP('$')

.CODE
MAIN PROC

MOV AX, @DATA
MOV DS,AX

MOV SI, OFFSET STR1  ;SI POINTS TO ARRAY

L1:
MOV AH,1 ;INPUT THE STRING
INT 21H

CMP AL,13 ; COMPARE TO CHECK IF INPUT IS ENTER KEY
JE PROGRAMEND ;IF EQUAL THEN JUMP TO PROGRAMEND

MOV[SI], AL ; MOV INPUT TO SI
INC SI; INCREMENT SI BY 1
JMP L1

PROGRAMEND:
MOV DX, OFFSET STR1 ; PRINT ARRAY
MOV AH,9 ;TO PRINT STRING
INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN


