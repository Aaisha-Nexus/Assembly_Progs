DOSSEG
;PROGRAM TO TAKE INPUT AND COMPARE IF EQUAL OR NOT
.MODEL SMALL
.STACK 100H
.DATA
  STR1 DB " NUMBERS ARE EQUAL$"                                             
  STR2 DB " NUMBERS ARE NOT EQUAL$"
.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX

SMOV DL, '3';STORE 3 IN DL TO COMPARE AGAINST
MOV AH, 1   ;USER INPUT A NUMBER
INT 21H
CMP DL,AL   ;COMPARE BOTH NUMBERS( BACKEND SUBTRACTION AND ZERO FLAG HAPPENS)

JE L1      ;JUMP ON L1 IF THE NUMBERS ARE EQUAL       
LEA DX, STR2   ;IF NUMBERS ARE NOT EQUAL THEN DISPLAY STR2 
MOV AH,9        
INT 21H         
MOV AH,4CH     ;ALSO EXIT THIS MODE AND END THE PROG 
INT 21H        

L1:
LEA DX,STR1   ;IF NUMBERS ARE EQUAL THEN DISPLAY STR1
MOV AH,9
INT 21H
MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
