DOSSEG
; PROGRAM TO PRINT 2 STRINGS ON TWO DIFF LINES
.MODEL SMALL
.STACK 100H
.DATA
  STR1 DB "ALLAH DOESN NOT BURDEN A SOUL$"
  STR2 DB "BEYOND THAT IT CAN BEAR$"
.CODE
MAIN PROC
 MOV AX, @DATA
 MOV DS, AX

 LEA DX, STR1   ;TO GET THE STRING MESSAGE
 MOV AH,9       ;TO PRINT STRING USE 9
 INT 21H

 MOV DX,10      ;FOR NEXT LINE AND CARRIAGE RETURN
 MOV AH,2
 INT 21H
 MOV DX,13
 MOV AH,2
 INT 21H

 LEA DX,STR2   ;TO READ SECOND STRING
 MOV AH,9
 INT 21H

 MOV AH,4CH   ;TO EXIT
 INT 21H

 MAIN ENDP
 END MAIN



