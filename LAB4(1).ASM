DOSSEG
;PRINT AN ARRAY USING LOOP
.MODEL SMALL
.STACK 100H
.DATA
 ARRAY DB 'K','A','R','A','C','H','I'
.CODE
MAIN PROC

MOV AX, @DATA
MOV DS, AX

MOV SI,OFFSET ARRAY ; SI POINTS TO ARRAY
MOV CX,7 ;COUNTER FOR LOOP

L1:
MOV DX, [SI] ;IT GETS VALUE AT ADDRESS SI
MOV AH,2    ;PRINT VALUE
INT 21H
INC SI ;INCREMENT THE LOOP BY 1
LOOP L1

MOV AH,4CH  
INT 21H

MAIN ENDP    
END MAIN
