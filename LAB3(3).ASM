DOSSEG
;PROGRAM TO PRINT ALPHABETS FROM UPPERCASE TO LOWERCASE
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
 MOV CX, 26 ;SET COUNTER TO LOOP 26 TIMES
 MOV DX, 65 ;TO START FROM 65 WHICH IS CAPITAL A

 UPPERCASE_LOOP:
 MOV AH,2
 INT 21H
 INC DX   ;INCREMENT BY 1 TO KEEP PRINTING ALL A-Z
 LOOP UPPERCASE_LOOP

 MOV DX,10     ;FOR NEW LINE AND CARRIAGE RETURN 
 MOV AH,2
 INT 21H
 MOV DX,13
 MOV AH,2
 INT 21H

 MOV CX,26  ;RESET COUNTER FOR UPPERCASE 
 MOV DX,65 ;

 LOWERCASE_LOOP:
 ADD DX,32   ;ADDING 32 TO 65 WILL GIVE 97 WHICH IS SMALL a AND SO ON
 MOV AH,2
 INT 21H
 SUB DX,32   ;adjust the alphabet back to uppercase b4 incrementing loop
 INC DX      ;INCREMENT TILL SMALL z
 LOOP LOWERCASE_LOOP
 

 ;EXIT
 MOV AH,4CH
 MAIN ENDP
 END MAIN
