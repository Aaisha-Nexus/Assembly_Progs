;PROGRAM TO DIVIDE 2 NUMBERS AND PRINT QUOTIENT AND REMAINDER
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
  QUO DB ?  ; VARIABLE DEFINE FOR QUOTIENT AND REMAINDER
  REM DB ?
.CODE
MAIN PROC
        MOV AX, @DATA
        MOV DS, AX

        MOV AX,10  ;DIVIDEND GOES TO AX
        MOV BL,3  ; DIVISOR GOES TO BL
        DIV BL     ;DIVIDE COMMAND

        MOV QUO, AL ;MOVE QUOTIENT (AL) TO QUO
        MOV REM, AH ;MOVE REMAINDER (AH) TO REM

        MOV DL, QUO ;MOVES QUO TO DL
        ADD DL,48 ;ADJUST ASCII
        MOV AH,2
        INT 21H

        MOV DL,REM   ;MOVES REMAINDER TO DL
        ADD DL,48
        MOV AH,2
        INT 21H

        ;EXIT THE PROG
        MOV AH,4CH    
        INT 21H

       
        MAIN ENDP
        END MAIN
        
